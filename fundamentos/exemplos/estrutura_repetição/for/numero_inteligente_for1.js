const n_inicio = parseFloat(prompt("Escolha um número inicial: "));
const n_fim = parseFloat(prompt("Escolha o número final: "));

for (let i = n_inicio; 
    
    n_inicio <= n_fim 
    
    ?
    
    i <= n_fim 
    
    : 

    i >= n_fim; 
    
    i += n_inicio <= n_fim 
    
    ? 
    
    1 : -1) {
    console.log(`Número: ${i}`);
}